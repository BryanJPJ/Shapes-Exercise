function hideElement(){
    document.getElementById("triangulo").style.visibility = "hidden";
}
function hideSquare(){
    document.getElementById("cuadrado").style.visibility = "hidden";
}
function hideCircle(){
    document.getElementById("circulo").style.visibility = "hidden";
}

function reset(){
    document.getElementById("triangulo").style.visibility = "visible";
    document.getElementById("cuadrado").style.visibility = "visible";
    document.getElementById("circulo").style.visibility = "visible";
}